export const applyBackgroundColor = (item, styles) => {
  if (item.id === 1) {
    return styles.backgroundColor1;
  }
  if (item.id === 2) {
    return styles.backgroundColor2;
  }
  if (item.id === 3) {
    return styles.backgroundColor3;
  }
  if (item.id === 4) {
    return styles.backgroundColor4;
  }
  if (item.id === 5) {
    return styles.backgroundColor5;
  }
};
